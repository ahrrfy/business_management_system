/** يُهرّب الأحرف الخاصة في نمط LIKE الخاص بـMySQL.
 *
 *  المشكلة: استعلامات البحث تَبني `like(field, `%${q}%`)` بقيمة المستخدم خاماً ⇒
 *  `%` و`_` يُفسَّران كحارفي بدل (wildcards) فيُسرّبان بحثاً غير مقصود
 *  (مثلاً «100%» يُطابق «1000» و«100abc»)، و`\` يُكسر الهروب.
 *
 *  الحل: تهريب `\` ثم `%` ثم `_` بـ`\` قبلها — جاهز للاستخدام مباشرةً مع
 *  `like(field, `%${escapeLike(q)}%`)` في drizzle. ترتيب التهريب مهم
 *  (الـ`\` أولاً وإلا ضوعف على نفسه).
 *
 *  مثال: `escapeLike('100%')` ⇒ `'100\\%'`.
 */
export function escapeLike(input: string): string {
  return input.replace(/\\/g, "\\\\").replace(/%/g, "\\%").replace(/_/g, "\\_");
}
